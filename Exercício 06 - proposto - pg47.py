# 2.6. Refaça o exercício 5 alterando-o de modo que a base e a altura do triângulo sejam lidas do teclado. Considere-as números reais. área de um triângulo


print("Cálculo da área de um triângulo")
base = float(input("\nDigite a medida da base do triângulo: "))
altura = float(input("\nDigite a altura do triângulo: "))

area = ( base * altura ) / 2

print("\nA área do triângulo é: " ,area)
 
